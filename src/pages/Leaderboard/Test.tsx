const limit = 4;
 
interface Data {
  rank: number;
  discord_user_id: number;
  study_time: number;
}
 
function createData(
  rank: number,
  discord_user_id: number,
  study_time: number
): Data {
  return { rank, discord_user_id, study_time };
}
 
interface HeadCell {
  disablePadding: boolean;
  id: keyof Data;
  label: string;
  numeric: boolean;
}
 
const headCells: HeadCell[] = [
  { id: 'rank', numeric: true, disablePadding: true, label: 'Rank' },
  { id: 'discord_user_id', numeric: true, disablePadding: false, label: 'Discord User Id' },
  { id: 'study_time', numeric: true, disablePadding: false, label: 'Study Time' },
];
 
function EnhancedTableHead() {
  return (
    <TableHead>
      <TableRow>
        {headCells.map((headCell) => (
          <TableCell
            key={headCell.id}
            align="center"
            padding={headCell.disablePadding ? 'none' : 'default'}
          >
            {headCell.id}
          </TableCell>
        ))}
      </TableRow>
    </TableHead>
  );
}
 
const useToolbarStyles = makeStyles((theme: Theme) =>
  createStyles({
    root: {
      paddingLeft: theme.spacing(2),
      paddingRight: theme.spacing(1),
    },
    highlight:
       theme.palette.type === 'light'
         ? {
           color: theme.palette.secondary.main,
           backgroundColor: lighten(theme.palette.secondary.light, 0.85),
         }
         : {
           color: theme.palette.text.primary,
           backgroundColor: theme.palette.secondary.dark,
         },
    title: {
      flex: '1 1 100%',
    },
  })
);
 
const useStyles = makeStyles((theme: Theme) =>
  createStyles({
    root: {
      width: '100%',
    },
    paper: {
      width: '75%',
      marginBottom: theme.spacing(2),
    },
    table: {
      minWidth: 500,
    },
    visuallyHidden: {
      border: 0,
      clip: 'rect(0 0 0 0)',
      height: 1,
      margin: -1,
      overflow: 'hidden',
      padding: 0,
      position: 'absolute',
      top: 20,
      width: 1,
    },
  })
);
 
const Leaderboard = ({ leaderboard }) => {
  const classes = useStyles();
  const [page, setPage] = React.useState(0);
  const [dense, setDense] = React.useState(false);
  const [rowsPerPage, setRowsPerPage] = React.useState(5);
 
  const handleChangePage = (event: unknown, newPage: number) => {
    setPage(newPage);
  };
 
  const handleChangeRowsPerPage = (event: React.ChangeEvent<HTMLInputElement>) => {
    setRowsPerPage(parseInt(event.target.value, 10));
    setPage(0);
  };
 
  const handleChangeDense = (event: React.ChangeEvent<HTMLInputElement>) => {
    setDense(event.target.checked);
  };
 
  const rows = leaderboard.map((user) => (
    createData(
      user.rank,
      user.discord_user_id,
      user.study_time
    ))
  );
 
  const emptyRows = rowsPerPage - Math.min(rowsPerPage, rows.length - page * rowsPerPage);
 
  return (
    <div className={classes.root}>
      <Paper className={classes.paper}>
        <TableContainer>
          <Table
            className={classes.table}
            aria-labelledby="tableTitle"
            size={dense ? 'small' : 'medium'}
            aria-label="enhanced table"
          >
            <EnhancedTableHead />
            <TableBody>
              {rows
                .slice(page * rowsPerPage, page * rowsPerPage + rowsPerPage)
                .map((row, index) => {
                  const labelId = `enhanced-table-checkbox-${index}`;
 
                  return (
                    <TableRow
                      hover
                      tabIndex={-1}
                      key={row.rank}
                    >
                      <TableCell align="center" component="th" scope="row" padding="none">
                         {row.rank}
                       </TableCell>
                      <TableCell align="center">{row.discord_user_id}</TableCell>
                      <TableCell align="center">{row.study_time}</TableCell>
                    </TableRow>
                  );
                })}
              {emptyRows > 0 && (
              <TableRow style={{ height: (dense ? 33 : 53) * emptyRows }}>
                <TableCell colSpan={6} />
              </TableRow>
              )}
            </TableBody>
          </Table>
        </TableContainer>
        <TablePagination
          rowsPerPageOptions={[5, 10, 25]}
          component="div"
          count={rows.length}
          rowsPerPage={rowsPerPage}
          page={page}
          onChangePage={handleChangePage}
          onChangeRowsPerPage={handleChangeRowsPerPage}
        />
      </Paper>
      <FormControlLabel
        control={<Switch checked={dense} onChange={handleChangeDense} />}
        label="Dense padding"
      />
    </div>
  );
};


export default Leaderboard;
